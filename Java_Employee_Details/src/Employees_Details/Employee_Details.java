    private void jbtnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnExportActionPerformed
      
        frame1 = new JFrame("Export");
        if(JOptionPane.showConfirmDialog(frame1, "Do you want to export all your data to a file?", "Employee Database System", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_NO_OPTION){
           DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
            try{
            File file = new File("/Users/saifilali/Desktop/JPMC/database.txt ");
           if(!file.exists()){
               file.createNewFile();
           }
           
           FileWriter fw = new FileWriter(file.getAbsoluteFile());
           BufferedWriter bw = new BufferedWriter(fw);
           bw.write("EmployeeID  |   FirstName  |   LastName  |  Salary  |  Designation  |  TechnicalSkills  |  Joining Date  |\n");
           for(int i = 0; i < jTable1.getRowCount(); i++){
               for(int j = 0; j < jTable1.getColumnCount(); j++){
                   
                   bw.write((String)jTable1.getModel().getValueAt(i,j)+" ");
                   bw.write("   |   ");
               }
               bw.write("\n\n");
           }
           bw.close();
           fw.close();
           JOptionPane.showMessageDialog(null,"Data Exported. Check JPMC folder");
           
          }catch(Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jbtnExportActionPerformed

