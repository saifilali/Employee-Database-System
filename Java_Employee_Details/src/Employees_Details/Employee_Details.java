    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRow()==-1){
            if(jTable1.getRowCount()==0){
                
                JOptionPane.showMessageDialog(null,"No data to delete", "Employee Database System", JOptionPane.OK_OPTION);
            }
            else{
               JOptionPane.showMessageDialog(null,"Select a row from below to delete", "Employee Database System", JOptionPane.OK_OPTION); 
            }
        }
        else{
            model.removeRow(jTable1.getSelectedRow());
        }
    }//GEN-LAST:event_jbtnDeleteActionPerformed
